num1 = float(input("please enter your first number "))
num2 = float(input("please enter your second number "))
operator = input ("please enter your operator + - / * ")
if operator == ("+"):
    print (str(num1 + num2)) 
if operator == ("-"):
    print (str(num1 - num2))
if operator == ("/"):
    print (str(num1 / num2))
if operator == ("*"):
    print (str(num1 * num2))
